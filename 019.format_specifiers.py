price1 = 3.14159



print(f"Price 1 is {price1:.2f}") # 2 characters behind the .
print(f"Price 1 is {price1:10}")  # total of 10 characters
print(f"Price 1 is {price1:010}") # total of 10 characters with 0 infront
print(f"Price 1 is {price1:<10}")  # total of 10 characters left justified
print(f"Price 1 is {price1:>10}")  # total of 10 characters right justified
print(f"Price 1 is {price1:^10}")  # total of 10 characters middle justified